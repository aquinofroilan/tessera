const today = new Date(MOCK_TODAY);
const daysFromToday = (offset: number) => {
    const d = new Date(today);
    d.setDate(d.getDate() + offset);
    return d.toISOString().slice(0, 10);
};
const isoDateTime = (offset: number) => {
    const d = new Date(today);
    d.setDate(d.getDate() + offset);
    d.setHours(11, 18, 0, 0);
    return d.toISOString();
};

const line = (accountId: string, description: string, amount: string): BillLineResponse => {
    const acc = expenseAccounts.find((a) => a.id === accountId)!;
    return {
        accountId,
        accountCode: acc.code,
        accountName: acc.name,
        amount,
        description,
    };
};

