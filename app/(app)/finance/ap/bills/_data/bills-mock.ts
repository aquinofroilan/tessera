function expand(seed: BillSeed): BillResponse {
    const { _createdOffset, ...rest } = seed;
    return {
        ...rest,
        organizationId: ORG_ID,
        exchangeRate: "1.0",
        baseCurrencyAmount: rest.totalAmount,
        baseCurrencyTaxAmount: rest.taxAmount,
        baseCurrencyAmountPaid: rest.amountPaid,
        createdAt: isoDateTime(_createdOffset),
        updatedAt: isoDateTime(_createdOffset + 1),
    };
}

